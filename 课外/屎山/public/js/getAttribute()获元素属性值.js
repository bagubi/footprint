//<a id="link" href="https://www.example.com" title="Example">访问示例网站</a>
const linkElement = document.getElementById("link");

// 获取 href 属性的值
const hrefValue = linkElement.getAttribute("href");
console.log(hrefValue); // 输出: https://www.example.com

// 获取 title 属性的值
const titleValue = linkElement.getAttribute("title");
console.log(titleValue); // 输出: Example

